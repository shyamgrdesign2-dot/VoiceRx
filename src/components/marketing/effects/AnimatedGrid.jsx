const VB = 2500;

// Tuning knobs (from the reference implementation).
const SPEED = 1050;
const CYCLE = 3.0;
const COMET_LEN = 200;
const COMET_T = 4;
const LINE_T = 3.5;
const CLIP_T = 5;
const INTERCEPT_TOL = 10;

function edgeAsRectD(x1, y1, x2, y2, t) {
  const dx = x2 - x1, dy = y2 - y1;
  const L = Math.hypot(dx, dy);
  if (L === 0) return "";
  const ux = dx / L, uy = dy / L;
  const px = -uy * t / 2, py = ux * t / 2;
  return (
    `M${(x1 + px).toFixed(2)},${(y1 + py).toFixed(2)}` +
    `L${(x2 + px).toFixed(2)},${(y2 + py).toFixed(2)}` +
    `L${(x2 - px).toFixed(2)},${(y2 - py).toFixed(2)}` +
    `L${(x1 - px).toFixed(2)},${(y1 - py).toFixed(2)}Z`
  );
}

// Group edges into LANES. Each lane is one line equation (H, V, /, \)
// that may contain multiple non-contiguous edges. The traveling pulse
// rides the lane's full extent; a per-lane clipPath hides any portion
// without an edge.
const LANES = (() => {
  const tagged = EDGES.map(([x1, y1, x2, y2]) => {
    const dx = x2 - x1, dy = y2 - y1;
    let kind, raw;
    if (Math.abs(dy) < 0.5) { kind = "H"; raw = y1; }
    else if (Math.abs(dx) < 0.5) { kind = "V"; raw = x1; }
    else if (dx * dy < 0) { kind = "/"; raw = y1 + x1; }
    else { kind = "\\"; raw = y1 - x1; }
    return { kind, raw, edge: [x1, y1, x2, y2] };
  });

  const out = [];
  for (const kind of ["H", "V", "/", "\\"]) {
    const items = tagged.filter((t) => t.kind === kind).sort((a, b) => a.raw - b.raw);
    if (!items.length) continue;
    let cluster = [items[0]];
    const flush = () => {
      const allPts = cluster.flatMap((c) => [
        [c.edge[0], c.edge[1]],
        [c.edge[2], c.edge[3]],
      ]);
      const intercept = cluster.reduce((s, c) => s + c.raw, 0) / cluster.length;
      let sx, sy, ex, ey;
      if (kind === "H") {
        const xs = allPts.map((p) => p[0]);
        sx = Math.max(...xs); sy = intercept;
        ex = Math.min(...xs); ey = intercept;
      } else if (kind === "V") {
        const ys = allPts.map((p) => p[1]);
        sx = intercept; sy = Math.min(...ys);
        ex = intercept; ey = Math.max(...ys);
      } else if (kind === "/") {
        const ys = allPts.map((p) => p[1]);
        const ymin = Math.min(...ys), ymax = Math.max(...ys);
        sx = intercept - ymin; sy = ymin;
        ex = intercept - ymax; ey = ymax;
      } else {
        const ys = allPts.map((p) => p[1]);
        const ymin = Math.min(...ys), ymax = Math.max(...ys);
        sx = ymin - intercept; sy = ymin;
        ex = ymax - intercept; ey = ymax;
      }
      const clipD = cluster
        .map((c) => edgeAsRectD(c.edge[0], c.edge[1], c.edge[2], c.edge[3], CLIP_T))
        .join("");
      out.push({ kind, intercept, sx, sy, ex, ey, clipD });
    };
    for (let i = 1; i < items.length; i++) {
      if (items[i].raw - cluster[cluster.length - 1].raw > INTERCEPT_TOL) {
        flush();
        cluster = [items[i]];
      } else {
        cluster.push(items[i]);
      }
    }
    flush();
  }

  const orderKey = { H: 0, V: 1, "/": 2, "\\": 3 };
  out.sort((a, b) => {
    if (orderKey[a.kind] !== orderKey[b.kind]) return orderKey[a.kind] - orderKey[b.kind];
    if (a.kind === "H") return a.sy - b.sy;
    if (a.kind === "V") return a.sx - b.sx;
    return a.intercept - b.intercept;
  });
  return out;
})();

const ALL_EDGES_D = EDGES.map(([x1, y1, x2, y2]) => `M${x1},${y1}L${x2},${y2}`).join("");

function LanePulse({ lane }) {
  const { sx, sy, ex, ey } = lane;
  const dx = ex - sx, dy = ey - sy;
  const len = Math.hypot(dx, dy);
  const ux = dx / len, uy = dy / len;
  const sxPad = sx - ux * COMET_LEN;
  const syPad = sy - uy * COMET_LEN;
  const exPad = ex + ux * COMET_LEN;
  const eyPad = ey + uy * COMET_LEN;
  const path = `M${sxPad.toFixed(1)},${syPad.toFixed(1)} L${exPad.toFixed(1)},${eyPad.toFixed(1)}`;

  const travel = (len + 2 * COMET_LEN) / SPEED;
  const activeFrac = Math.min(travel / CYCLE, 0.999);

  return (
    <rect
      x={-COMET_LEN / 2}
      y={-COMET_T / 2}
      width={COMET_LEN}
      height={COMET_T}
      fill="url(#cometGrad)"
    >
      <animateMotion
        dur={`${CYCLE}s`}
        repeatCount="indefinite"
        rotate="auto"
        keyPoints="0;1;1"
        keyTimes={`0;${activeFrac.toFixed(3)};1`}
        calcMode="linear"
        path={path}
      />
    </rect>
  );
}

